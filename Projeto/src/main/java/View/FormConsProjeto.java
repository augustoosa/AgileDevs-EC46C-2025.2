    private void btBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btBuscarActionPerformed
        // 1. Coleta os filtros da tabela principal
        String nome = cxNomeFiltro.getText();
        String status = cxStatusFiltro.getText();
        String gerenteNome = cxGerenteResponsavelFiltro.getText(); // <-- 1. Variável renomeada

        // 2. Chama o controller de Projeto para buscar com os filtros
        List<Projeto> listaFiltrada = controller.buscarPorFiltros(nome, status, gerenteNome); // <-- 2. Chamada atualizada

        // 3. Preenche a tabela de PROJETOS com os resultados
        preencherTabelaProjetos(listaFiltrada);
    }//GEN-LAST:event_btBuscarActionPerformed

