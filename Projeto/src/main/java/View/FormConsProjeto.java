    private void carregarTabelaProjetos() {
        List<Projeto> listaCompleta = controller.listaTodos();
        preencherTabelaProjetos(listaCompleta);
    }
    private void preencherTabelaProjetos(List<Projeto> lista) {
        modTblResultados.setRowCount(0);
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");

        for (Projeto p : lista) {
            String nomeGerente = (p.getGerenteResponsavel() != null) ? p.getGerenteResponsavel().getNome() : "N/D";
            String dataInicio = (p.getDataInicio() != null) ? sdf.format(p.getDataInicio()) : "N/D";
            String prazoFinal = (p.getPrazoFinal() != null) ? sdf.format(p.getPrazoFinal()) : "N/D";
            
            modTblResultados.addRow(new Object[]{
                p.getIdProjeto(),
                p.getNomeProjeto(),
                p.getStatus(),
                dataInicio,
                prazoFinal,
                nomeGerente
            });
        }
    }
    private void preencherTabelaProgramadores(List<Funcionario> lista) {
        modTblEquipeProg.setRowCount(0);
        for (Funcionario f : lista) {
            if (f instanceof Programador) {
                Programador p = (Programador) f;
                // Mostra nome e CPF para diferenciar
                modTblEquipeProg.addRow(new Object[]{
                    p.getNome() + " (CPF: " + p.getCpf() + ")"
                });
            }
        }
    }
    private void preencherTabelaAnalistas(List<Funcionario> lista) {
        modTblEquipeAnalista.setRowCount(0);
        for (Funcionario f : lista) {
            if (f instanceof AnalistaDeDados) {
                AnalistaDeDados a = (AnalistaDeDados) f;
                modTblEquipeAnalista.addRow(new Object[]{
                    a.getNome() + " (CPF: " + a.getCpf() + ")"
                });
            }
        }
    }
    
    
    
    
